/** SeText text editor style names. */
public enum SeTextTextEditorStyleNames {
    /** Default style. */
    DEFAULT,

    /** Identifier style. */
    IDENTIFIER,

    /** Description style. */
    DESCRIPTION,

    /** Single-line comment style. */
    COMMENT_SL,

    /** Multi-line comment style. */
    COMMENT_ML,

    /** String literal style. */
    STRING,

    /** Keyword style. */
    KEYWORD,
}
